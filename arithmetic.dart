void main() {
  int a = 40;
  int b = 9;
  print("Addition:  ${a + b}");
  print("Subtraction:  ${a - b}");
  print("Multiplication:  ${a * b}");
  print("Division:  ${a / b}");
  print("Division:  ${a / b}"); // it will return value in integer.
  print("Modulus:  ${a % b}");
  print("Increment:  ${++a}");
  print("Decrement:  ${--b}");
}
